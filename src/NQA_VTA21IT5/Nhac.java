package NQA_VTA21IT5;

public class Nhac {
	
	private int mabaihat;
	private String tenbaihat;
	private String tencasi;
	private int thoiluong;
	private String theloai;
	private String loibaihat;
	
	public Nhac(int mabaihat, String tenbaihat, String tencasi, int thoiluong, String theloai, String loibaihat) {
		super();
		this.mabaihat = mabaihat;
		this.tenbaihat = tenbaihat;
		this.tencasi = tencasi;
		this.thoiluong = thoiluong;
		this.theloai = theloai;
		this.loibaihat = loibaihat;
	}

	public int getMabaihat() {
		return mabaihat;
	}

	public void setMabaihat(int mabaihat) {
		this.mabaihat = mabaihat;
	}

	public String getTenbaihat() {
		return tenbaihat;
	}

	public void setTenbaihat(String tenbaihat) {
		this.tenbaihat = tenbaihat;
	}

	public String getTencasi() {
		return tencasi;
	}

	public void setTencasi(String tencasi) {
		this.tencasi = tencasi;
	}

	public int getThoiluong() {
		return thoiluong;
	}

	public void setThoiluong(int thoiluong) {
		this.thoiluong = thoiluong;
	}

	public String getTheloai() {
		return theloai;
	}

	public void setTheloai(String theloai) {
		this.theloai = theloai;
	}

	public String getLoibaihat() {
		return loibaihat;
	}

	public void setLoibaihat(String loibaihat) {
		this.loibaihat = loibaihat;
	}
	
	

	
}
